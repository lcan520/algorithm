/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        ListNode temp = head.next;
        ListNode pre = head;
        int l = 1;
        while (temp != null) {
            temp = temp.next;
            l++;
        }
        if (l == 1) {
            return null;
        }
        n = l - n - 1;
        if (n == -1) {
            return head.next;
        }

        temp = head.next;
        for (int i = 0; i < n; i++) {
            pre = temp;
            temp = temp.next;
        }
        pre.next = temp.next;
        temp.next = null;
        return head;
    }
}
